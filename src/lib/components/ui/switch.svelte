<script lang="ts">
  let {
    checked = $bindable(false),
    disabled = false,
    id,
    label = '',
  }: { checked?: boolean; disabled?: boolean; id?: string; label?: string } = $props();
</script>

<button
  role="switch"
  aria-checked={checked}
  {disabled}
  type="button"
  onclick={() => { if (!disabled) checked = !checked; }}
  class="relative inline-flex h-5 w-9 shrink-0 cursor-pointer rounded-full border-2 border-transparent
    transition-colors duration-200
    focus:outline-none focus:ring-2 focus:ring-cyber-cyan/30
    disabled:cursor-not-allowed disabled:opacity-40
    {checked ? 'bg-cyber-cyan' : 'bg-bg-border'}"
>
  <span
    class="pointer-events-none inline-block h-4 w-4 rounded-full bg-white shadow-sm
      transition-transform duration-200
      {checked ? 'translate-x-4' : 'translate-x-0'}"
  />
</button>
